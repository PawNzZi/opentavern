// Safe t() helper - state may not exist yet during early load
function t(key) {
    try {
        var lang = (window.state && state.settings && state.settings.lang) || 'zh-CN';
        var dict = i18n[lang];
        if (!dict) dict = i18n['en'];
        return dict[key] !== undefined ? dict[key] : (i18n['en'][key] || key);
    } catch (e) {
        return i18n['en'][key] || key;
    }
}

// Make available globally
window.i18n = i18n;
window.t = t;
